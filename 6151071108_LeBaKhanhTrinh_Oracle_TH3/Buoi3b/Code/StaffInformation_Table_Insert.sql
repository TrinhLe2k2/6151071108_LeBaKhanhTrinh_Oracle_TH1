-- T?o b?ng DEPARTMENT
CREATE TABLE DEPARTMENT (
    DeptNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    DeptName VARCHAR(255),
    Location VARCHAR(255),
    NumOfStaff INT
);
/
-- T?o b?ng EMPLOYEE
CREATE TABLE EMPLOYEE (
    EmpNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    EmpName VARCHAR(255),
    Salary INT,
    DeptNo INT,
    CONSTRAINT fk_employee_department FOREIGN KEY (DeptNo) REFERENCES DEPARTMENT(DeptNo) ON DELETE CASCADE
);
/
-- T?o b?ng PROJECT
CREATE TABLE PROJECT (
    ProjNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    ProjName VARCHAR(255),
    ProjType VARCHAR(255),
    Budget INT
);
/
-- T?o b?ng WORKS
CREATE TABLE WORKS (
    EmpNo INT,
    ProjNo INT,
    Role VARCHAR(255),
    CONSTRAINT fk_work_employee FOREIGN KEY (EmpNo) REFERENCES EMPLOYEE(EmpNo) ON DELETE CASCADE,
    CONSTRAINT fk_work_project FOREIGN KEY (ProjNo) REFERENCES PROJECT(ProjNo) ON DELETE CASCADE
);
/
