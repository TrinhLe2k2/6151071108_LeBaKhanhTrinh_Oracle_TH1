

-- T?o b?ng HOTEL
CREATE TABLE HOTEL (
    hotelNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    hotelName VARCHAR2(255),
    city VARCHAR2(255),
    managerName VARCHAR2(255),
    numOfStaff INT
);
/
-- T?o b?ng ROOM
CREATE TABLE ROOM (
    roomNo INT PRIMARY KEY,
    hotelNo INT,
    type VARCHAR2(50),
    price NUMBER(10, 2),
    description CLOB,
    CONSTRAINT fk_room_hotel
    FOREIGN KEY (hotelNo)
    REFERENCES HOTEL(hotelNo)
    ON DELETE CASCADE
);
/
-- T?o b?ng GUEST
CREATE TABLE GUEST (
    guestNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    guestName VARCHAR2(255),
    address CLOB,
    phone VARCHAR2(20)
);
/
-- T?o b?ng BOOKING
CREATE TABLE BOOKING (
    bookingNo INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    guestNo INT,
    roomNo INT,
    hotelNo INT,
    dateBooking DATE,
    dateCheckin DATE,
    dateCheckout DATE,
    description CLOB,
    numberGuest INT,
    CONSTRAINT fk_booking_guest FOREIGN KEY (guestNo) REFERENCES GUEST(guestNo) ON DELETE CASCADE,
    CONSTRAINT fk_booking_room FOREIGN KEY (roomNo) REFERENCES ROOM(roomNo) ON DELETE CASCADE,
    CONSTRAINT fk_booking_hotel FOREIGN KEY (hotelNo) REFERENCES HOTEL(hotelNo) ON DELETE CASCADE
);
/
