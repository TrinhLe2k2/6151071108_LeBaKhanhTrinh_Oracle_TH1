
CREATE TABLE Hospitals (
    HospitalID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    HospitalName VARCHAR(255),
    Address VARCHAR(255),
    City VARCHAR(255)
);
/
CREATE TABLE Doctors (
    DoctorID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    DoctorName VARCHAR(255),
    DepartmentName VARCHAR(255),
    HospitalID INT,
    City VARCHAR(255),
    Salary NUMBER(10, 2),
    CONSTRAINT fk_Doctor_Hospital FOREIGN KEY (HospitalID) REFERENCES Hospitals(HospitalID) ON DELETE CASCADE
);
/
CREATE TABLE Patients (
    PatientID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    PatientName VARCHAR(255),
    Sex VARCHAR(10),
    BirthDate DATE,
    Address VARCHAR(255),
    City VARCHAR(255),
    Phone VARCHAR(20),
    Disease VARCHAR(255),
    Bed INT,
    Room INT,
    HospitalID INT,
    CONSTRAINT fk_Patient_Hospital FOREIGN KEY (HospitalID) REFERENCES Hospitals(HospitalID) ON DELETE CASCADE
);
/
CREATE TABLE Treatments (
    TreatmentID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    DoctorID INT,
    PatientID INT,
    TypeofTreatment VARCHAR(255),
    CONSTRAINT fk_Treatment_Doctor FOREIGN KEY (DoctorID) REFERENCES Doctors(DoctorID) ON DELETE CASCADE,
    CONSTRAINT fk_Treatment_Patient FOREIGN KEY (PatientID) REFERENCES Patients(PatientID) ON DELETE CASCADE
);
/

